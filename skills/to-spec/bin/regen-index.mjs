#!/usr/bin/env node
// regen-index.mjs <specs-root>
//
// Regenerates per-area README.md bodies and the top-level README.md
// from frontmatter declared in:
//   - <specs-root>/<AREA>/*.spec.md
//   - <specs-root>/<AREA>/README.md   (area metadata: area, name, description)
//
// Authored preface (anything between the frontmatter and the GENERATED marker)
// is preserved. Everything below the marker is rewritten on every run.
//
// Exits non-zero on missing frontmatter, missing required fields, unsupported
// frontmatter line, duplicate IDs, id/filename/folder mismatches, or PRD path
// that does not resolve to an existing file. The caller (to-spec) should treat
// non-zero exit as "spec not published — fix and re-run".

import { readdirSync, readFileSync, writeFileSync, statSync, existsSync } from 'node:fs';
import { join } from 'node:path';

const MARKER = '<!-- GENERATED by to-spec/bin/regen-index.mjs — edit frontmatter above; do not edit below this line -->';

function die(msg) {
  console.error(`regen-index: ${msg}`);
  process.exit(1);
}

// Parse leading `---\n...\n---\n` frontmatter. Flat frontmatter only:
// `key: value` (string) and `key: [a, b, c]` (array of strings). No multiline
// scalars, no nested maps. Anything else is rejected.
function parseFrontmatter(text, path) {
  if (!text.startsWith('---\n')) return { fm: null, rest: text };
  const end = text.indexOf('\n---\n', 4);
  if (end === -1) die(`${path}: opening --- without closing ---`);
  const body = text.slice(4, end);
  const rest = text.slice(end + 5);
  const fm = {};
  for (const raw of body.split('\n')) {
    const line = raw.trimEnd();
    if (!line || line.startsWith('#')) continue;
    const m = line.match(/^([a-zA-Z_][a-zA-Z0-9_]*):\s*(.*)$/);
    if (!m) die(`${path}: malformed frontmatter line: ${line}`);
    const [, key, val] = m;
    if (val.startsWith('[') && val.endsWith(']')) {
      const inner = val.slice(1, -1).trim();
      fm[key] = inner ? inner.split(',').map(s => s.trim().replace(/^["']|["']$/g, '')) : [];
    } else {
      fm[key] = val.replace(/^["']|["']$/g, '');
    }
  }
  return { fm, rest };
}

function splitOnMarker(rest) {
  const idx = rest.indexOf(MARKER);
  if (idx === -1) return { preface: rest.trim() };
  return { preface: rest.slice(0, idx).trim() };
}

function buildFile(fm, preface, generated) {
  const parts = [];
  if (fm && Object.keys(fm).length) {
    parts.push('---');
    for (const [k, v] of Object.entries(fm)) {
      parts.push(Array.isArray(v) ? `${k}: [${v.join(', ')}]` : `${k}: ${v}`);
    }
    parts.push('---', '');
  }
  if (preface) parts.push(preface, '');
  parts.push(MARKER, '', generated);
  return parts.join('\n').replace(/\n+$/, '\n');
}

function requireKeys(fm, keys, path) {
  if (!fm) die(`${path}: missing frontmatter (expected --- ... --- block at top)`);
  for (const k of keys) {
    if (fm[k] === undefined || fm[k] === '') die(`${path}: missing required frontmatter field: ${k}`);
  }
}

// Escape `|` so cell contents cannot break the markdown table, and collapse
// internal whitespace so a multi-line value stays single-row.
function cell(value) {
  return String(value).replaceAll('|', '\\|').replace(/\s+/g, ' ').trim();
}

function main() {
  const root = process.argv[2];
  if (!root) die('usage: regen-index.mjs <specs-root>');
  if (!existsSync(root)) die(`${root}: not found`);

  const areas = readdirSync(root)
    .filter(name => /^[A-Z]{2,3}$/.test(name))
    .filter(name => statSync(join(root, name)).isDirectory())
    .sort();

  const areaSummaries = [];

  for (const area of areas) {
    const areaDir = join(root, area);
    const readmePath = join(areaDir, 'README.md');
    if (!existsSync(readmePath)) {
      die(`${readmePath} missing — create it with frontmatter (area, name, description) first`);
    }
    const { fm, rest } = parseFrontmatter(readFileSync(readmePath, 'utf8'), readmePath);
    requireKeys(fm, ['area', 'name', 'description'], readmePath);
    if (fm.area !== area) die(`${readmePath}: area frontmatter "${fm.area}" does not match folder "${area}"`);

    const specs = readdirSync(areaDir)
      .filter(n => n.endsWith('.spec.md'))
      .sort()
      .map(name => {
        const p = join(areaDir, name);
        const { fm: sfm } = parseFrontmatter(readFileSync(p, 'utf8'), p);
        requireKeys(sfm, ['id', 'title', 'synopsis', 'prd'], p);
        if (!/^[A-Z]{2,3}-\d{3}$/.test(sfm.id)) die(`${p}: id "${sfm.id}" must match <AREA>-<NNN>`);
        if (sfm.id.split('-')[0] !== area) die(`${p}: id area "${sfm.id}" does not match folder "${area}"`);
        if (!name.startsWith(sfm.id + '-')) die(`${p}: filename must start with id "${sfm.id}-"`);
        if (sfm.synopsis.length > 140) die(`${p}: synopsis exceeds 140 chars (${sfm.synopsis.length})`);
        const prdPath = join(areaDir, sfm.prd);
        if (!existsSync(prdPath)) die(`${p}: prd path "${sfm.prd}" does not resolve to an existing file (looked at ${prdPath})`);
        return { file: name, ...sfm };
      });

    const ids = new Set();
    for (const s of specs) {
      if (ids.has(s.id)) die(`${areaDir}: duplicate id "${s.id}"`);
      ids.add(s.id);
    }

    const table = specs.length
      ? [
          '| ID | Feature | Synopsis |',
          '| :- | :------ | :------- |',
          ...specs.map(s => `| [${cell(s.id)}](./${s.file}) | ${cell(s.title)} | ${cell(s.synopsis)} |`),
        ].join('\n')
      : '_No features yet._';

    const generated = `## Features\n\n${table}\n`;
    const { preface } = splitOnMarker(rest);
    writeFileSync(readmePath, buildFile(fm, preface, generated));

    areaSummaries.push({ area, name: fm.name, description: fm.description, count: specs.length });
  }

  // Top-level README
  const topPath = join(root, 'README.md');
  let topFm = null;
  let topPreface = '';
  if (existsSync(topPath)) {
    const { fm, rest } = parseFrontmatter(readFileSync(topPath, 'utf8'), topPath);
    topFm = fm;
    topPreface = splitOnMarker(rest).preface;
  }
  const topTable = areaSummaries.length
    ? [
        '| Area | Name | Description | Features |',
        '| :--- | :--- | :---------- | -------: |',
        ...areaSummaries.map(a => `| [${cell(a.area)}](./${a.area}/) | ${cell(a.name)} | ${cell(a.description)} | ${a.count} |`),
      ].join('\n')
    : '_No areas yet._';
  const topGenerated = `# Specifications\n\n## Areas\n\n${topTable}\n`;
  writeFileSync(topPath, buildFile(topFm, topPreface, topGenerated));

  const total = areaSummaries.reduce((n, a) => n + a.count, 0);
  console.log(`regen-index: ${areas.length} area(s), ${total} spec(s) — OK`);
}

main();
